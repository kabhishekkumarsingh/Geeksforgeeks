class Solution
{
    public:
    //Function to find a continuous sub-array which adds up to a given number.
    vector<int> subarraySum(vector<int>arr, int n, long long s)
    {
    int currentSum=arr[0],i=0,j;
    for(int j=1;j<=n;j++){
        while(currentSum>s && i<j-1){
            currentSum=currentSum-arr[i];
            i++;
        }
        if(currentSum==s){
            vector<int>result{i+1,j};
            return result;
        }
        if(j<n) currentSum=currentSum+arr[j];
    }
    return {-1};
    }
};

